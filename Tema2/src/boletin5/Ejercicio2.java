package boletin5;

public class Ejercicio2 {

	public static void main(String[] args) {
		
		// Creamos la variable numero y la establecemos a 0
		int numero = 0;
		
		// Creamos el bucle: sumamos 2 al numero mientras que el numero sea menor que 200
		do {
			// Sumamos 2 al numero
			numero += 2;
			
			// Imprimimos el numero
			System.out.println(numero);
		
		// Mientras que el numero sea menor que 200, el bucle se seguira ejecutando
		} while (numero < 200);

		
	}

}
